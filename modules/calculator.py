class Calculator:

    def __init__ (self, _n1, _n2):
        self.n1 = _n1
        self.n2 = _n2

    def add(self):
        return self.n1 + self.n2

    def subtract(self):
        return self.n1 - self.n2
    
    def multiply(self):
        return self.n1 * self.n2

    def divide(self):
        return self.n1 / self.n2